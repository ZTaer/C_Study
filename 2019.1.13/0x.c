#include <stdio.h>

int main(){
	
	char a='S';
	int b=10;

	// 控制指针指向变量内存地址( &符号 取地址运算符 )
	char *ch = &a;
	int *in = &b;

	// 指针其实就是间接访问数据: 指针 - 指向变量内存地址(&符号) - 变量指向存储数据的内存地址 - 访问到数据(*符号)
	printf("内存地址导引(指针): ch(a) = %c,in(b) = %d\n",*ch,*in); // ( *符号 在这里为取值运算符, *符号 在定义类型中为创建指针 )
	printf("原变量: a = %c,b = %d\n",a,b);

	
	// 通过指针改变变量存储的值( 上放已经确定指向的变量 )
	*ch = 'A';
	*in = 666;

	printf("a=%c,b=%d",a,b);
	

	return 0;
}
